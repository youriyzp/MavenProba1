package net.ukr.kiyashko.lab2;


public class DoubleCalculator
{


    public Double sum(Double c, Double d) {
        return c + d;
    }
    public Double  sub(Double c, Double d) {
        return c - d;
    }
    public Double  mul(Double c, Double d) {
        return c * d;
    }
    public Double div(Double c, Double d) {
        return c / d;
    }

}
